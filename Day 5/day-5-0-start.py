# Loops -> Things that have to happen over and over again
# Indentation is really important!
fruits = ["Apple", "Orange", "Mango"]

for tunda in fruits:
    print(tunda)
    print("Here's an " + tunda + " pie") # loops through the list and adds pie
# Everytime the for loop runs the variable tunda is assigned to an item in the fruits list i.e
#   Apple
#   Here's an Apple pie
# It excutes until the last item in the List
