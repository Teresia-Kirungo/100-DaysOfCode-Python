dirty_dozen = ["Strawberries", "Spinach", "Kale", "Apples", "Grapes", "Lettuce", "Cabbages", "Pears", "Peaches"]

# Out of the dirty dozens, the fruits are:
fruits = ["Strawberries", "Apples", "Grapes", "Pears", "Peaches"]

# Out of the dirty dozens, the veggies are are:
