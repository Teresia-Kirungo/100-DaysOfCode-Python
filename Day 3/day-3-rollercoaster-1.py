# Write a program that determines if a user is tall enough to ride on the rollercoaster, having the standard height as 120.

print("Welcome to the rollercoaster!")
height = int(input("What is your height in cm? "))

if height > 120:
    print("You are allowed to ride the rollercoaster!")
else:
    print("I am sorry come back a few cm taller.")
